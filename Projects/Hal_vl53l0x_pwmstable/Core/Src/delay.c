#include "delay.h"
#include "main.h"
static uint8_t  Times_us=1;//1us需要计的次数
static uint16_t Times_ms=0;//1ms需要计的次数
//SYSCLK=72M,SYSCLK/8=9M
void delay_init(uint8_t SYSCLK)
{
	HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK_DIV8);	//选择外部时钟  HCLK/8
	Times_us=SYSCLK/8;	//Times_us=9次	    
	Times_ms=(uint16_t)Times_us*1000;//Times_ms=9000次
}
//SYSCLK=72M,SYSCLK/8=9M,即计一次数为9分之一 微秒，1us需要计数9次
//所以SysTick->LOAD=nus*SYSCLK/8=nus*9=Count
//Count<2^24，Max_Count=16777215
//最大nus=Max_Count/9=16777215/9=1864135us
//即最大nus为1864135
void delay_us(uint32_t nus)
{		
	uint32_t temp;	    	 
	SysTick->LOAD=(nus*Times_us); //时间加载	  		 
	SysTick->VAL=0x00;        //清空计数器
	SysTick->CTRL=0x01 ;      //开始倒数 	 	
	do
	{
		temp=SysTick->CTRL;
	}
	while(temp&0x01&&!(temp&(1<<16)));//等待时间到达   
	SysTick->CTRL=0x00;       //关闭计数器
//	SysTick->VAL =0X00;       //清空计数器	 
}
//SYSCLK=72M,SYSCLK/8=9M,1微秒需计数9次，则1毫秒需计数9000次
//所以SysTick->LOAD=nms*9000=Count
//Count<2^24，Max_Count=16777215
//最大nms=Max_Count/9000=16777215/9000=1864.135ms，
//即最大nms=1864
void delay_ms(uint16_t nms)
{	 		  	  
	uint32_t temp;		   
	SysTick->LOAD=167999;//时间加载(SysTick->LOAD为24bit)
	SysTick->VAL =0x00;           
	SysTick->CTRL=0x01 ;   
	do
	{
		temp=SysTick->CTRL;
	}
	while(temp&0x01&&!(temp&(1<<16)));  
	SysTick->CTRL=0x00;       
	SysTick->VAL =0X00;       	    
}
void start_Time(void)
{
	  SysTick->CTRL=0x00; //关闭计数器  
    SysTick->LOAD=(Times_us*40000);//40ms定时
	  SysTick->CTRL=0x01 ;  //清16位，使用外部时钟，无中断，开启定时器计数
}
void Get_Time(float *time)//得到时间单位为ms
{
	  SysTick->CTRL=0x00; //关闭计数器  
    time[0]=((float)(40000*Times_us-SysTick->VAL))/(Times_us*1000);//获取时间
	   	SysTick->VAL =0x00;          //清计数器 
}



